#include <unistd.h>

int	main(int ac, char *av[])
{
	int	k;
	int	m;

	k = 0;
	m = 1;
	while (m < ac)
	{
		while (av[m][k] != '\0')
		{
			write(1, &av[m][k], 1);
			k++;
		}
		k = 0;
		m++;
		write (1, "\n", 1);
	}
	return (0);
}
